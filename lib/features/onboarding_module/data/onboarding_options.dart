// Static option data for the onboarding selection screens
// (country / currency / favourite games).
//
// Kept as plain const lists so the screens stay lag-free with a
// `ListView.builder` over already-in-memory data.

/// A selectable country. [flag] is derived from the ISO code, so callers
/// never hard-code emoji.
class CountryOption {
  const CountryOption(this.name, this.code);

  /// Display name, e.g. "India".
  final String name;

  /// ISO-3166 alpha-2 code, e.g. "IN".
  final String code;

  /// Flag emoji built from the two regional-indicator symbols for [code].
  String get flag {
    final upper = code.toUpperCase();
    if (upper.length != 2) return '🏳️';
    const base = 0x1F1E6; // regional indicator symbol "A"
    final a = base + (upper.codeUnitAt(0) - 0x41);
    final b = base + (upper.codeUnitAt(1) - 0x41);
    return String.fromCharCode(a) + String.fromCharCode(b);
  }
}

/// A selectable currency.
class CurrencyOption {
  const CurrencyOption(this.code, this.symbol, this.name);

  /// ISO-4217 code, e.g. "USD".
  final String code;

  /// Display symbol, e.g. "\$".
  final String symbol;

  /// Display name, e.g. "US Dollar".
  final String name;
}

/// A selectable game shown as an emoji chip.
class GameOption {
  const GameOption(this.id, this.name, this.emoji);

  final String id;
  final String name;
  final String emoji;
}

